-- Объединение «Зачем · полное» и «Зачем · презентация» в один столбец «Зачем и для чего делаем»

UPDATE b2b_product_status_row
SET cells = (
    cells
    - 'Зачем и для чего делаем полное описание'
    - 'Зачем и для чего делаем для презентации'
) || jsonb_build_object(
    'Зачем и для чего делаем',
    COALESCE(
        NULLIF(BTRIM(cells->>'Зачем и для чего делаем'), ''),
        NULLIF(BTRIM(cells->>'Зачем и для чего делаем для презентации'), ''),
        NULLIF(BTRIM(cells->>'Зачем и для чего делаем полное описание'), ''),
        ''
    )
);
